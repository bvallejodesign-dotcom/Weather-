export function calcularEstadisticas(pronostico) {
  const maximas = pronostico.map(d => d.max)
  const minimas = pronostico.map(d => d.min)
  const todas = [...maximas, ...minimas]
  return {
    temp_max: Math.max(...maximas),
    temp_min: Math.min(...minimas),
    promedio_max: Math.round(maximas.reduce((a, b) => a + b, 0) / maximas.length),
    promedio_min: Math.round(minimas.reduce((a, b) => a + b, 0) / minimas.length),
    rango_termico: Math.max(...maximas) - Math.min(...minimas),
    promedio_general: Math.round(todas.reduce((a, b) => a + b, 0) / todas.length)
  }
}

export function celsiusAFahrenheit(c) {
  return Math.round((c * 9/5) + 32)
}
